      <span class="intro">We offer programs for children age 2 through pre-kindergarten age. The children benefit from the use of well-defined age appropriate daily curriculum that direct their learning experience. These experiences are, in turn, enhanced by the Christian values environment that is the core of the Park's Edge Preschool and it's caring teachers. Activities include block building, creative arts, library and language sensory wet and dry, music, movement, woodworking and much more. Each day offers opportunities for the preschool child to learn through play.</span><br>
      <br>

      Children learn by doing, therefore our classrooms are set up to create a supportive and interesting environment that promotes learning. Park's Edge Preschool knows the importance of developing the "whole child", including socio-emotional development, cognitive development, and large motor development. All of these important parts of development will be taken into consideration when developing Developmentally Appropriate Curriculum. The play you see your child participating in while at school today influences the development of the basic foundation for life-long learning. Children need to learn at their own pace; all children do not learn the same way or at the same time. Good habits and attitudes are being developed, which will make a difference throughout their lives.<br>
      <br>

      <strong class="bluetext">Some of the goals we focus on attaining with each child are:</strong> problem solving, classifying objects (pre-reading), expansion of verbal skills, acquiring love of books, experience a sense of self-esteem, exhibit a positive attitude toward life, to demonstrate cooperative, pro-social behavior, enhance and refine fine and large motor skills, to use all senses in learning, acquire beginning writing skills, and develop beginning reading skills. By offering a variety of experiences along with introducing new skills, children will learn to love learning, which is the first step in growth.<br>
      <br>

      We ensure that children are exposed to all necessary skills that are required for entrance to kindergarten. The early  childhood experts teach us "abstract, rote learning is not appropriate for young children." Just because children can learn these skills does not mean that they understand them. If we want children to be successful in school now and in the future, we have to teach them to think, to solve problems, and to get along with others. These abilities are acquired when children are encouraged to explore their environment actively, to solve real problems that have meaning for them, and to work cooperatively with others to complete tasks. This is what you see when you walk in and see the children "playing".<br>
      <br>

      Children will be exposed to reading, writing, and phonics, through a print rich environment, letter of the week, beginning to practice writing their name, recognizing letters and numbers, sequencing, and lots of books!<br>
